#!/usr/bin/env python
from nltk.corpus import wordnet as wn
from itertools import chain

word=raw_input("Enter the word to search about:")
syns = wn.synsets(word)
print("Name")
print(syns[0].name())
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("Actual Name")      
print(syns[0].lemmas()[0].name())
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("Defintion")      
print(syns[0].definition())
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("Example Set")      
print(syns[0].examples())
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("Hypernym Set")      
for i,j in enumerate(wn.synsets(word)): 
    print('Hypernyms:',i,j, ', '.join(list(chain(*[l.lemma_names() for l in j.hypernyms()]))))
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
synonyms = []
antonyms = []

for syn in wn.synsets(word):
    for l in syn.lemmas():
        synonyms.append(l.name())
        if l.antonyms():
            antonyms.append(l.antonyms()[0].name())
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("Synonym Set")      
print(set(synonyms))
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("------------------------------------------------------------------------")
print("Antonym Set")
print(set(antonyms))
